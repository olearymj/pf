				<p class="project-description">
					The Secret Garden was a locative media installation developed for a final project in the MSc in Interactive Digital Media 2011/2012 in Trinity College Dublin (TCD), Ireland. Working on the theme of ‘open spaces’, we aimed to offer users a positive opportunity to disconnect from the ubiquitous communication networks that today constitute a ‘virtual’ layer, inseparable from the physical domain of urban life. 
					<br><br>	
					The geographic focus of the project was a small botanical research garden situated on the campus of TCD, which is inaccessible to the public and generally unnoticed by passersby despite its visibility from the busy street it borders. We designed an interactive web application using audio recorded from different elements in the garden and visualizations of data collected by weather sensors placed in the garden. A local wireless network was established in the garden such that access to the live web application requires physical presence in the space as well as disconnection from larger wireless networks connected to the Internet. By mildly subverting the usual relationship between network or mobile technologies and space and connectivity, we hoped to offer a chance for ‘virtual’ disconnection, and possibly the opportunity for mindful reflection on the pervasiveness of these technologies in our lives.
